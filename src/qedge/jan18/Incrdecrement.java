package qedge.jan18;

public class Incrdecrement {

	public static void main(String[] args) {
		int i=100;
		System.out.println(i++);
		System.out.println(i);
		System.out.println(++i);
		System.out.println(--i);
		System.out.println(i--);
		System.out.println(i);
		int j=300;
		System.out.println(j+=10);
		System.out.println(j++);
		int k=400;
		System.out.println(k-=10);
		System.out.println(k--);
		System.out.println(k);
		System.out.println(--k);

	}

}
