package qedge.jan18;

public class Ifelse {

	public static void main(String[] args)throws Throwable {
		// verify given marks pass or fail
		int marks=15;
		if(marks>=35)
		{
			System.out.println("pass");
			
		}else
		{
			Thread.sleep(1000);
			System.out.println("fail");
		}

	}

}
